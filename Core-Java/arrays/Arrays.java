package arrays;

public class Arrays {
	
	public static void main(String args[]){
	
		int[] numbers = new int[3];
		numbers[0] = 0;
		numbers[1] = 1;
		numbers[2] = 2;
		
		for(int i = 0 ; i < 3 ; i++){
			System.out.println(numbers[i]);
		}
	
		String[] names = new String[2];
		names[0] = "John";
		names[1] = "Jane";
		
		System.out.println(names[1]);
		
		
	}
}
